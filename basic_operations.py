# 2. Comparison operations
x = 10
y = 20

greater_than = x > y
less_than = x < y
equal_to = x == y
not_equal_to = x != y
greater_than_or_equal_to = x >= y
less_than_or_equal_to = x <= y

print("\nComparison Operations:")
print("Greater Than:", greater_than)
print("Less Than:", less_than)
print("Equal To:", equal_to)
print("Not Equal To:", not_equal_to)
print("Greater Than or Equal To:", greater_than_or_equal_to)
print("Less Than or Equal To:", less_than_or_equal_to)

# 3. Logical operations
p = True
q = False

and_operation = p and q
or_operation = p or q
not_operation = not p

print("\nLogical Operations:")
print("AND Operation:", and_operation)
print("OR Operation:", or_operation)
print("NOT Operation:", not_operation)

# 4. Bitwise operations
m = 10  # 1010
n = 7   # 0111

bitwise_and = m & n
bitwise_or = m | n
bitwise_xor = m ^ n
bitwise_left_shift = m << 1
bitwise_right_shift = m >> 1
bitwise_not = ~m

print("\nBitwise Operations:")
print("Bitwise AND:", bitwise_and)
print("Bitwise OR:", bitwise_or)
print("Bitwise XOR:", bitwise_xor)
print("Bitwise Left Shift:", bitwise_left_shift)
print("Bitwise Right Shift:", bitwise_right_shift)
print("Bitwise NOT:", bitwise_not)

# 5. Membership operations
list_a = [1, 2, 3, 4, 5]

membership_in = 3 in list_a
membership_not_in = 6 not in list_a

print("\nMembership Operations:")
print("Membership IN:", membership_in)
print("Membership NOT IN:", membership_not_in)
